    public int getIDSQL(DBComponents db, User user) {
        int userID = -1;

        try {
            String sql = "SELECT userID FROM realestate.users WHERE username = ?";
            db.getCon();
            db.setPst(db.getCon().prepareStatement(sql));
            db.getPst().setString(1, user.getUsername());
            try (ResultSet resultSet = db.getPst().executeQuery()) {
                if (resultSet.next()) {
                    userID = resultSet.getInt("UserID");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(UserInterface.class.getName()).log(Level.SEVERE, null, ex);
        }

        return userID;
    }
            
    



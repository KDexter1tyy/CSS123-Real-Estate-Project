    private void jButtonRegisterFormActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegisterFormActionPerformed
        UserAction register= new UserActionRegisterStrategy();
        String password= jPasswordFieldPassword.getText();
        String passwordConf= jPasswordFieldConfirm.getText();
        
        
        if (!password.equals(passwordConf)){
            JFrame frame = new JFrame();
            JOptionPane.showMessageDialog(frame, "Passwords don't match! Re-type the Password.");
            return;
        }
        
        try {
            DBComponents db = DBComponents.getInstance();
            db.getCon();
            register.UserActionNow(db, jTextFieldUsername.getText(), jPasswordFieldPassword.getText(), user);
            user.setUserid(getIDSQL(db,user));
        } catch (SQLException ex) {
            Logger.getLogger(UserInterface.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_jButtonRegisterFormActionPerformed

